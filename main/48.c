int fun(int a, int b)
{
/*a:12     b:34
	c:1324  
 */
	int ag;   ag = (a % 10) * 10;
	int as;   as = (a / 10) * 1000;
	int bg;   bg = b % 10;
	int bs;   bs = (b / 10) * 100;

	return ag + as + bs + bg;
}